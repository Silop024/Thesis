public class Shape
{
    /**
     * The identifier of the shape, corresponds to the name of a variable or a function for example.
     */
    public int id;

    /**
     * Where it is positioned in the image, starts from top left, ends bottom right.
     */
    public int index;

    /**
     * What type of construct it is, if it is some pre-defined construct.
     */
    public Construct construct;
}
